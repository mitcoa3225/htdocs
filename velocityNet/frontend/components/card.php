// card file.


function startCard($title = '', $subtitle = '', $classes = '') {
    echo '<div class="card ' . $classes . '">';
    
    if ($title || $subtitle) {
        echo '<div class="card-header">';
        if ($title) {
            echo '<h3 class="card-title">' . htmlspecialchars($title) . '</h3>';
        }
        if ($subtitle) {
            echo '<p class="card-subtitle">' . htmlspecialchars($subtitle) . '</p>';
        }
        echo '</div>';
    }
    
    echo '<div class="card-body">';
}

//run end card
function endCard() {
    echo '</div>'; // Close card-body
    echo '</div>'; // Close card
}

//run render stat card
function renderStatCard($title, $value, $icon = '', $iconType = 'primary') {
    echo '<div class="stat-card">';
    
    if ($icon) {
        echo '<div class="stat-icon ' . $iconType . '">';
        echo $icon;
        echo '</div>';
    }
    
    echo '<div class="stat-content">';
    echo '<p class="stat-label">' . htmlspecialchars($title) . '</p>';
    echo '<p class="stat-value">' . htmlspecialchars($value) . '</p>';
    echo '</div>';
    
    echo '</div>';
}

